public class PuntoGeometrico {
    private double Y;
    private double X;
    public PuntoGeometrico(){
        this.Y = 0;
        this.X = 0;
    }

    public void desplazarPunto(double x, double y){
        setX(getX()+x);
        setY(getY()+y);
    }
    public double distanciaEuclidea(double x, double y){
        return  Math.sqrt( (  (getX()-x) * (getX()-x) ) + ( (getY()-y) * (getY()-y) ) );
    }
    public PuntoGeometrico(double y, double x) {
        setY(y);
        setX(x);
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }
}
